const getTranslation = (lang, key) => {
  const parts = key.split('.');
  let current = translations[lang];
  for (const part of parts) {
    if (!current || typeof current !== 'object') return undefined;
    current = current[part];
  }
  return current;
};

const translate = (lang, key, params = {}) => {
  const fallback = getTranslation('en', key);
  let value = getTranslation(lang, key) ?? fallback ?? key;
  if (typeof value === 'string' && Object.keys(params).length > 0) {
    value = Object.entries(params).reduce(
      (acc, [token, replacement]) => acc.replaceAll(`{{${token}}}`, String(replacement)),
      value,
    );
  }
  return value;
};

export { translations, translate };
